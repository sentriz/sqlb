package main

import (
	"errors"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"io"
	"os"
	"path/filepath"
	"strings"
	"unicode"
)

func main() {
	types, dest, err := parseArgs(os.Args[1:])
	if err != nil {
		fmt.Fprintf(os.Stderr, "%s\n%s\n", err, "usage: sqlbgen [ type T1 [ generated F1 F2 ... ] ]... -- output.gen.go")
		os.Exit(1)
	}

	goFile := os.Getenv("GOFILE")
	if goFile == "" {
		fmt.Fprintln(os.Stderr, "need GOFILE")
		os.Exit(1)
	}
	goPackage := os.Getenv("GOPACKAGE")
	if goPackage == "" {
		fmt.Fprintln(os.Stderr, "need GOPACKAGE")
		os.Exit(1)
	}

	fset := token.NewFileSet()
	node, err := parser.ParseFile(fset, goFile, nil, parser.ParseComments)
	if err != nil {
		fmt.Fprintf(os.Stderr, "parse file: %v\n", err)
		os.Exit(1)
	}

	destf, err := os.Create(dest)
	if err != nil {
		fmt.Fprintf(os.Stderr, "create dest file: %v\n", err)
		os.Exit(1)
	}
	defer destf.Close()

	fmt.Fprintf(destf, "// Code generated by \"%s %s\"; DO NOT EDIT.\n", filepath.Base(os.Args[0]), strings.Join(os.Args[1:], " "))
	fmt.Fprintf(destf, "\npackage %s\n", goPackage)
	fmt.Fprintf(destf, "\nimport (\n")
	fmt.Fprintf(destf, "\t\"database/sql\"\n")
	fmt.Fprintf(destf, "\t\"fmt\"\n")
	fmt.Fprintf(destf, ")\n")

	for _, tc := range types {
		fields := structFields(node, tc.name)
		writeType(destf, *tc, fields)
	}
}

func parseArgs(args []string) ([]*typeConfig, string, error) {
	var types []*typeConfig
	var dest string

	var cur *typeConfig
	var cmd string
	for _, arg := range args {
		switch arg {
		case "type", "generated", "--":
			cmd = arg
			continue
		}
		switch cmd {
		case "type":
			cur = &typeConfig{name: arg}
			types = append(types, cur)
		case "generated":
			cur.generated = append(cur.generated, arg)
		case "--":
			dest = arg
		}
	}
	if len(types) == 0 {
		return nil, "", errors.New("no types specified")
	}
	if dest == "" {
		return nil, "", errors.New("missing -- output.gen.go")
	}
	return types, dest, nil
}

type typeConfig struct {
	name      string
	generated []string
}

func structFields(node *ast.File, typeName string) []string {
	for n := range ast.Preorder(node) {
		typeSpec, ok := n.(*ast.TypeSpec)
		if !ok || typeSpec.Name.Name != typeName {
			continue
		}
		structType, ok := typeSpec.Type.(*ast.StructType)
		if !ok {
			continue
		}
		var fields []string
		for _, field := range structType.Fields.List {
			for _, name := range field.Names {
				fields = append(fields, name.Name)
			}
		}
		return fields
	}
	return nil
}

func writeType(w io.Writer, tc typeConfig, fields []string) {
	r := strings.ToLower(string([]rune(tc.name)[0]))

	fmt.Fprintf(w, "\nfunc _() {\n")
	fmt.Fprintf(w, "\t// Validate the struct fields haven't changed. If this doesn't compile you probably need to `go generate` again.\n")
	fmt.Fprintf(w, "\tvar %s %s\n", r, tc.name)
	var fieldRefs []string
	for _, field := range fields {
		fieldRefs = append(fieldRefs, fmt.Sprintf("%s.%s", r, field))
	}
	fmt.Fprintf(w, "\t_ = %s{%s}\n", tc.name, strings.Join(fieldRefs, ", "))
	fmt.Fprintf(w, "}\n")

	fmt.Fprintf(w, "\nfunc (%s) IsGenerated(c string) bool {\n", tc.name)
	if len(tc.generated) > 0 {
		var cases []string
		for _, col := range tc.generated {
			cases = append(cases, fmt.Sprintf("%q", toSnake(col)))
		}
		fmt.Fprintf(w, "\tswitch c {\n")
		fmt.Fprintf(w, "\tcase %s:\n", strings.Join(cases, ", "))
		fmt.Fprintf(w, "\t\treturn true\n")
		fmt.Fprintf(w, "\t}\n")
	}
	fmt.Fprintf(w, "\treturn false\n")
	fmt.Fprintf(w, "}\n")

	fmt.Fprintf(w, "\nfunc (%s %s) Values() []sql.NamedArg {\n", r, tc.name)
	var namedArgs []string
	for _, f := range fields {
		namedArgs = append(namedArgs, fmt.Sprintf("sql.Named(\"%s\", %s.%s)", toSnake(f), r, f))
	}
	fmt.Fprintf(w, "\treturn []sql.NamedArg{%s}\n", strings.Join(namedArgs, ", "))
	fmt.Fprintf(w, "}\n")

	fmt.Fprintf(w, "\nfunc (%s *%s) ScanFrom(columns []string, rows *sql.Rows, buf []any) error {\n", r, tc.name)
	fmt.Fprintf(w, "\tfor _, col := range columns {\n")
	fmt.Fprintf(w, "\t\tswitch col {\n")
	for _, f := range fields {
		fmt.Fprintf(w, "\t\tcase \"%s\":\n", toSnake(f))
		fmt.Fprintf(w, "\t\t\tbuf = append(buf, &%s.%s)\n", r, f)
	}
	fmt.Fprintf(w, "\t\tdefault:\n")
	fmt.Fprintf(w, "\t\t\treturn fmt.Errorf(\"unknown column name %%q\", col)\n")
	fmt.Fprintf(w, "\t\t}\n")
	fmt.Fprintf(w, "\t}\n")
	fmt.Fprintf(w, "\treturn rows.Scan(buf...)\n")
	fmt.Fprintf(w, "}\n")
}

func toSnake(s string) string {
	var result strings.Builder
	runes := []rune(s)
	for i, char := range runes {
		if i > 0 && unicode.IsUpper(char) {
			prev := runes[i-1]
			if unicode.IsLower(prev) || unicode.IsDigit(prev) || (i+1 < len(runes) && unicode.IsLower(runes[i+1])) {
				result.WriteRune('_')
			}
		}
		result.WriteRune(unicode.ToLower(char))
	}
	return result.String()
}
